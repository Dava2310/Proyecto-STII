    private void SeleccionarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarBTActionPerformed
        if(!MateriaSecatxt.getText().isEmpty() ){
            Tabla.setVisible(false);
            seleccionado = true;
            ModificarBT.setEnabled(true);
            NuevoBT.setEnabled(false);
        }else if(NuevoBT.isSelected()){
            JOptionPane.showMessageDialog(null, "TIENE EL BOTON NUEVO ACTIVADO, DESACTIVELO PRIMERO", "ERROR", JOptionPane.ERROR_MESSAGE);
            SeleccionarBT.setSelected(false);   
        }else if(MateriaSecatxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "SELECCIONE UN ELEMENTO DE LA TABLA", "ERROR", JOptionPane.ERROR_MESSAGE);
            SeleccionarBT.setSelected(false);
        }else if (!SeleccionarBT.isSelected()){
            Tabla.setVisible(true);
            seleccionado = false;
            NuevoBT.setEnabled(true);
            ModificarBT.setEnabled(false);
        }
        
    }//GEN-LAST:event_SeleccionarBTActionPerformed

