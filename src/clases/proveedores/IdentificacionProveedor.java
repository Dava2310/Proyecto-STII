    private boolean verificacionCompleta(){
        boolean condicion = true;
        String identificacion = objetoVerificador.quitarEspacios(Cedulatxt.getText());
        /*
            AQUI HACEMOS TODAS LAS VERIFICACIONES CORRESPONDIENTES AL CAMPO DE CEDULA 
            QUE TIENE QUE INGRESAR EL USUARIO
        
            1- Si esta vacio.
            2- Si no esta vacio, que cumpla con el rango de digitos.
            3- Si cumple con el rango de digitos, que todos sean numericos.
            4- Si cumple todo lo anterior, que no se encuentre en el sistema.
        */
        if(Cedulatxt.getText().isEmpty()){
            Cedulatxt.setBorder(borde_rojo);
            JOptionPane.showMessageDialog(null, "INGRESE UNA IDENTIFICACION", "ERROR", JOptionPane.ERROR_MESSAGE);
            condicion = false;
        } else if(!Cedulatxt.getText().isEmpty()){
            if(identificacion.length() < 7){
                condicion = false;
                Cedulatxt.setBorder(borde_rojo);
                JOptionPane.showMessageDialog(null, "LA IDENTIFICACION DEL PROVEEDOR DEBE ESTAR ENTRE 7 Y 8 DIGITOS", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else if(!(identificacion.length() < 7)){
                if(!p.comprobacionIdentificacion(identificacion)){
                    condicion = false;
                    Cedulatxt.setBorder(borde_rojo);
                    JOptionPane.showMessageDialog(null, "TODOS LOS DIGITOS DEBEN SER NUMERICOS", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if(p.comprobacionIdentificacion(identificacion)){
                    //Ahora hay que buscar que no se encuentre en el sistema
                    String tipoIdentificacion = TipoCedula.getSelectedItem().toString();
                    String identificacion_completa = tipoIdentificacion + identificacion;
                    try {
                        if(modo == 1){
                            if(p.buscarIdentificacion(identificacion_completa)){ 
                                condicion = false;
                                Cedulatxt.setBorder(borde_rojo);
                                JOptionPane.showMessageDialog(null, "YA HAY UN PROVEEDOR REGISTRADO CON ESTA IDENTIFICACION", "ERROR", JOptionPane.ERROR_MESSAGE); 
                            } else if(!p.buscarIdentificacion(identificacion_completa)){
                                Cedulatxt.setBorder(borde_default);
                            }
                        } else if(modo == 2){
                            if(p.buscarProveedorActivo(identificacion_completa)){
                                Cedulatxt.setBorder(borde_default);
                            } else if(!p.buscarProveedorActivo(identificacion_completa) || !p.buscarIdentificacion(identificacion_completa)){
                                condicion = false;
                                Cedulatxt.setBorder(borde_rojo);
                                JOptionPane.showMessageDialog(null, "ESTE PROVEEDOR NO ESTA REGISTRADO O ACTIVO EN EL SISTEMA", "ERROR", JOptionPane.ERROR_MESSAGE); 
                            }
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(IdentificacionProveedor.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        Cedulatxt.setText(identificacion);
        return condicion;
    }
    
    private void BotonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEnterActionPerformed
        boolean procedio = false;
        tipoIdentificacion = TipoCedula.getSelectedIndex();
        String t_identificacion = TipoCedula.getSelectedItem().toString();
        identificacion = objetoVerificador.quitarEspacios(Cedulatxt.getText());
        String identificacion_completa = t_identificacion + identificacion;

        //SE TIENE QUE PRIMERO VERIFICAR QUE HAYA INGRESADO EL USUARIO UNA CEDULA
        //EN EL CASO DE QUE HAYA INGRESADO UNA CEDULA, SE VERIFICA QUE SEA DE LA CANTIDAD DE DIGITOS NECESARIOS (7 u 8)
        if (verificacionCompleta()) {
            if (modo == 1) {
                try {
                    //SE VERIFICA QUE LA BUSQUEDA DE LA IDENTIFICACION RETORNE UN FALSE
                    //SIGNIFICANDO QUE NO HAY NADIE REGISTRADO POR AHORA CON ESA IDENTIFICACION
                    //PARA DAR PASO A LA CREACION DE UN NUEVO PROVEEDOR SIN QUE SE REPITA LA CEDULA
                    if (!(p.buscarIdentificacion(identificacion_completa))) {
                        Cedulatxt.setBorder(borde_default);
                        CP = new CreacionProveedor();
                        CP.identificacion = this.identificacion;
                        CP.tipoIdentificacion = this.tipoIdentificacion;
                        CP.setVisible(true);
                        procedio = true;
                    } else if(p.buscarIdentificacion(identificacion_completa)){
                        JOptionPane.showMessageDialog(null, "YA EXISTE UN PROVEEDOR CON ESTA IDENTIFICACION", "BUSQUEDA DE PROVEEDOR", JOptionPane.PLAIN_MESSAGE);
                        Cedulatxt.setBorder(borde_rojo);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(IdentificacionProveedor.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (modo == 2) {
                try {
                    //SE VERIFICA QUE LA BUSQUEDA DE LA IDENTIFICACION RETORNE UN TRUE
                    //DE ESTA MANERA EL ANTICIPO TRABAJA CON UN CODIGO DE PROVEEDOR VALIDO
                    //PARA DAR PASO A LA CREACION DE UN NUEVO ANTICIPO
                    if (p.buscarProveedorActivo(identificacion_completa)) {
                        Cedulatxt.setBorder(borde_default);
                        CA = new CrearAnticipo();
                        CA.identificacion = this.identificacion;
                        CA.identificacion_completa = identificacion_completa;
                        CA.tipo_identificacion = this.tipoIdentificacion;
                        CA.setVisible(true);
                        procedio = true;
                    } else if (!p.buscarProveedorActivo(identificacion_completa) && p.buscarIdentificacion(identificacion_completa)){
                        JOptionPane.showMessageDialog(null, "EL PROVEEDOR NO ESTA ACTIVO EN EL SISTEMA", "BUSQUEDA DE PROVEEDOR", JOptionPane.ERROR_MESSAGE);
                        Cedulatxt.setBorder(borde_rojo);
                    } else if (!p.buscarIdentificacion(identificacion_completa)){
                        JOptionPane.showMessageDialog(null, "EL PROVEEDOR NO SE ENCUENTRA REGISTRADO EN EL SISTEMA", "BUSQUEDA DE PROVEEDOR", JOptionPane.ERROR_MESSAGE);
                        Cedulatxt.setBorder(borde_rojo);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(IdentificacionProveedor.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (procedio) {
                this.dispose();
            }
        } 

    }//GEN-LAST:event_BotonEnterActionPerformed

