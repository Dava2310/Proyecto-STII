    private void SiguienteBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SiguienteBTActionPerformed
        int tipo_busqueda = TipoBusqueda.getSelectedIndex();
        //se elige a que interfaz se quiere ingresar(consultar o modificar)
        if (modo == 1) {
            CP = new ConsultarProveedor();
            if (tipo_busqueda == 0) {
                CP.modo_busqueda = 0;
            } else if (tipo_busqueda == 1) {
                CP.modo_busqueda = 1;
            } else if (tipo_busqueda == 2) {
                CP.modo_busqueda = 2;
            }
            CP.setVisible(true);
            this.dispose();
        } else if (modo == 2) {
            MEP = new ModificarEliminarProveedor();
            if (tipo_busqueda == 0) {
                MEP.modo_busqueda = 0;
            } else if (tipo_busqueda == 1) {
                MEP.modo_busqueda = 1;
            } else if (tipo_busqueda == 2) {
                MEP.modo_busqueda = 2;
            }
            MEP.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_SiguienteBTActionPerformed

